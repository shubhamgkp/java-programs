package collection;

import java.util.HashSet;
import java.util.Set;

public class SetHashSet {

	public static void main(String[] args) {
		
		Set<String> hashs = new HashSet<String>();
		hashs.add("Data1");
		hashs.add("Data2");
		hashs.add("Data3");
		hashs.add("Data4");
		hashs.add("Data5");
		System.out.println(hashs);
		
	}
}
